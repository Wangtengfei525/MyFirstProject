package com.coolcloud.sacw.exchange.entity;

public class PropertyExchange {
	public String id;
	public String property_id;
	public String property_status;
	public String property_status_code;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getProperty_id() {
		return property_id;
	}
	public void setProperty_id(String property_id) {
		this.property_id = property_id;
	}
	public String getProperty_status() {
		return property_status;
	}
	public void setProperty_status(String property_status) {
		this.property_status = property_status;
	}
	public String getProperty_status_code() {
		return property_status_code;
	}
	public void setProperty_status_code(String property_status_code) {
		this.property_status_code = property_status_code;
	}
	
	
}
